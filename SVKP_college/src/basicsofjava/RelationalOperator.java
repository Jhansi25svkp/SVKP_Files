package basicsofjava;

public class RelationalOperator {

	public static void main(String[] args) {
		int a=10,b=5;
		 System.out.println("a==b"+(a==b)); //equal to //false
		 System.out.println("a!=b"+(a!=b)); //not equals to//true
		 System.out.println("a>b"+(a>b)); //greater than //true
		 System.out.println("a<b"+(a<b)); //less than //false
		 System.out.println("a>=b"+(a>=b)); //greater than equals to //true
		 System.out.println("a<=b"+(a<=b));  //less than equals to //false
	}

}
